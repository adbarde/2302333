#include<iostream>
#include<cmath>
using namespace std;
int main()
{
    int a=10,b=4;
    cout<<"SQRT OF "<<b<<sqrt(b)<<endl; 
    cout<<"max value is "<<max(a,b)<<endl; 
    cout<<"min value is "<<min(a,b)<<endl; 
    cout<<"4.6 is rounded to "<<round(4.6)<<endl; 
    cout<<"power of 10 "<<pow(10,2)<<endl; 
    cout<<"ceil value of 2.3 "<<ceil(2.3)<<endl; 
    cout<<"floor value of 5.7"<<floor(5.7)<<endl;
    cout<<"log value of "<<b<<log(b)<<endl;  
    cout<<"abs value of -3 is "<<abs(-3)<<endl; 
    return 0;
}